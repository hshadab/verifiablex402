/// Build the transaction integrity classifier model.
///
/// Input [1,24]: normalized transaction features (see TransactionFeatures::to_normalized_vec)
/// Output [1,5]: [genuine, low_activity, scripted, circular, wash_trading]
pub fn tx_integrity_model() -> Model {
    const SCALE: i32 = 7;
    let mut b = ModelBuilder::new(SCALE);

    let input = b.input(vec![1, 24], 1);

    // Layer 1: [1,24] x [36,24] -> [1,36]
    let mut w1 = Tensor::new(Some(W1), &[36, 24]).unwrap();
    w1.set_scale(SCALE);
    let w1_const = b.const_tensor(w1, vec![36, 24], 1);

    let mm1 = b.matmult(input, w1_const, vec![1, 36], 1);
    let mm1_rescaled = b.div(128, mm1, vec![1, 36], 1);

    let mut b1 = Tensor::new(Some(B1), &[1, 36]).unwrap();
    b1.set_scale(SCALE);
    let b1_const = b.const_tensor(b1, vec![1, 36], 1);
    let biased1 = b.poly(PolyOp::Add, mm1_rescaled, b1_const, vec![1, 36], 1);

    let relu1 = b.relu(biased1, vec![1, 36], 1);

    // Layer 2: [1,36] x [36,36] -> [1,36]
    let mut w2 = Tensor::new(Some(W2), &[36, 36]).unwrap();
    w2.set_scale(SCALE);
    let w2_const = b.const_tensor(w2, vec![36, 36], 1);

    let mm2 = b.matmult(relu1, w2_const, vec![1, 36], 1);
    let mm2_rescaled = b.div(128, mm2, vec![1, 36], 1);

    let mut b2 = Tensor::new(Some(B2), &[1, 36]).unwrap();
    b2.set_scale(SCALE);
    let b2_const = b.const_tensor(b2, vec![1, 36], 1);
    let biased2 = b.poly(PolyOp::Add, mm2_rescaled, b2_const, vec![1, 36], 1);

    let relu2 = b.relu(biased2, vec![1, 36], 1);

    // Layer 3 (output): [1,36] x [5,36] -> [1,5]
    let mut w3 = Tensor::new(Some(W3), &[5, 36]).unwrap();
    w3.set_scale(SCALE);
    let w3_const = b.const_tensor(w3, vec![5, 36], 1);

    let mm3 = b.matmult(relu2, w3_const, vec![1, 5], 1);
    let mm3_rescaled = b.div(128, mm3, vec![1, 5], 1);

    let mut b3 = Tensor::new(Some(B3), &[1, 5]).unwrap();
    b3.set_scale(SCALE);
    let b3_const = b.const_tensor(b3, vec![1, 5], 1);
    let output = b.poly(PolyOp::Add, mm3_rescaled, b3_const, vec![1, 5], 1);

    b.take(vec![input.0], vec![output])
}

